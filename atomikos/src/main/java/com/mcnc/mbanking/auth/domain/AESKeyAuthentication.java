package com.mcnc.mbanking.auth.domain;

import java.io.Serializable;

public class AESKeyAuthentication implements Serializable {
	
	private static final long serialVersionUID = -5662924591486617632L;
	
	private String secretKey;
	
	public AESKeyAuthentication() {
	}
	
	public AESKeyAuthentication(String secretKey) {
		this.secretKey = secretKey;
	}

	public String getSecretKey() {
		return secretKey;
	}

	public void setSecretKey(String secretKey) {
		this.secretKey = secretKey;
	}

	@Override
	public String toString() {
		return "AESKeyAuthentication [secretKey=" + secretKey + "]";
	}
	
}
