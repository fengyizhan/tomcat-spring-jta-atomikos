package com.mcnc.mbanking.auth.domain;

import java.util.Date;

public class Account {
	private Long accountId;
	private String accountNumber;
	private String password;
	private String verifiedCode;
	private Date createdDateTime;
	private boolean isDeleted;
	private Date deletedDateTime;
	private Long customerId;
	public Long getAccountId() {
		return accountId;
	}
	public void setAccountId(Long accountId) {
		this.accountId = accountId;
	}
	public String getAccountNumber() {
		return accountNumber;
	}
	public void setAccountNumber(String accountNumber) {
		this.accountNumber = accountNumber;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getVerifiedCode() {
		return verifiedCode;
	}
	public void setVerifiedCode(String verifiedCode) {
		this.verifiedCode = verifiedCode;
	}
	public Date getCreatedDateTime() {
		return createdDateTime;
	}
	public void setCreatedDateTime(Date createdDateTime) {
		this.createdDateTime = createdDateTime;
	}
	public boolean isDeleted() {
		return isDeleted;
	}
	public void setDeleted(boolean isDeleted) {
		this.isDeleted = isDeleted;
	}
	public Date getDeletedDateTime() {
		return deletedDateTime;
	}
	public void setDeletedDateTime(Date deletedDateTime) {
		this.deletedDateTime = deletedDateTime;
	}
	public Long getCustomerId() {
		return customerId;
	}
	public void setCustomerId(Long customerId) {
		this.customerId = customerId;
	}
	@Override
	public String toString() {
		return "AccountDto [accountId=" + accountId + ", accountNumber=" + accountNumber + ", password=" + password
				+ ", verifiedCode=" + verifiedCode + ", createdDateTime=" + createdDateTime + ", isDeleted=" + isDeleted
				+ ", deletedDateTime=" + deletedDateTime + ", customerId=" + customerId + "]";
	}
}
