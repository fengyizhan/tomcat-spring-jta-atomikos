package com.mcnc.mbanking.auth.dto;

/**
 * Represent Response code in ResponseMessage
 * @author sayseakleng
 *
 */
public enum ResponseCode {
	/**
	 * Common Error code
	 */
	E0001("An error occurred"),
	
	/**
	 * Invalid session
	 */
	E0002("Invalid session"),
	
	
	/**
	 * Invalid encrypted data
	 */
	E0003("Invalid encrypted data");
	
	
	private String message;
	
	ResponseCode(String message) {
		this.message = message;
	}
	
	public String getMessage() {
		return message;
	}
	
}
